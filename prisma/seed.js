function zarLabel(value) {
  return new Intl.NumberFormat('en-ZA', {
    style: 'currency',
    currency: 'ZAR',
    maximumFractionDigits: 0,
  }).format(value);
}

async function upsertManyById(model, items) {
  for (const item of items) {
    await model.upsert({
      where: { id: item.id },
      update: item,
      create: item,
    });
  }
}

